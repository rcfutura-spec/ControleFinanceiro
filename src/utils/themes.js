export function getTheme(id) {
  return THEMES.find(t => t.id === id) || THEMES[0]
}

export function applyTheme(theme) {
  const root = document.documentElement
  const c = theme.colors

  root.style.setProperty('--c-bg900', c.bg900)
  root.style.setProperty('--c-bg800', c.bg800)
  root.style.setProperty('--c-bg700', c.bg700)
  root.style.setProperty('--c-bg600', c.bg600)
  root.style.setProperty('--c-bg500', c.bg500)
  root.style.setProperty('--c-accent', c.accent)
  root.style.setProperty('--c-accent-light', c.accentLight)
  root.style.setProperty('--c-accent-dark', c.accentDark)
  root.style.setProperty('--c-accent-glow', c.accentGlow)
  root.style.setProperty('--c-text', c.text)
  root.style.setProperty('--c-text-muted', c.textMuted)
  root.style.setProperty('--c-text-dim', c.textDim)
  root.style.setProperty('--c-success', c.success)
  root.style.setProperty('--c-warning', c.warning)
  root.style.setProperty('--c-danger', c.danger)

  document.body.style.background = c.bg900
  document.body.style.color = c.text

  // Set color-scheme for native form elements (date picker, scrollbar, etc.)
  const isLight = theme.id === 'arctic-frost'
  root.style.colorScheme = isLight ? 'light' : 'dark'
}
